export default function App() {
  //uplifting the items state to the common parent of form and packing list function
  const [items, setItems] = useState([]);

  //this function will create and merge the new input with the existing items array
  function handleAddItems(item) {
    setItems((items) => [...items, item]);
  }

  function handleDeleteItem(id) {
    setItems((items) => items.filter((item) => item.id !== id));
  }

  function handleToggleItem(id) {
    setItems((items) =>
      items.map((item) =>
        item.id === id ? { ...item, packed: !item.packed } : item
      )
    );
  }

  function handleEmptyArray() {
    const confirmDelete = window.confirm("Are you sure to clear the list?");

    if (confirmDelete) {
      setItems([]);
    }
  }

  return (
    <div className="app">
      <Logo />
      <Form onAddItems={handleAddItems} />
      <PackingList
        items={items}
        onDeleteItems={handleDeleteItem}
        onToggleItems={handleToggleItem}
        onClearItems={handleEmptyArray}
      />
      <Stats items={items} />
    </div>
  );
}
